<div>
  <h1>COMO DENUNCIAR?</h1>

  <p>
    Os principais canais de denúncia para reportar casos de maus-tratos, animais
    em cativeiro, suspeiras de tráfico de animais, etc, são:
  </p>

  <ul>
    <li>
      <strong>IBAMA:</strong>
      o Instituto Brasileiro do Meio Ambiente e dos Recursos Naturais Renováveis
      é o órgão federal responsável pelo controle e fiscalização do uso dos recursos
      naturais, incluindo a fauna. Para denunciar crimes ambientais, incluindo maus-tratos
      e tráfico de animais silvestres, é possível utilizar o
      <strong>telefone 0800 61 8080</strong>
      ou o <strong>formulário disponível no site do IBAMA</strong>.
    </li>
    <li>
      <strong>Polícia Ambiental:</strong>
      a Polícia Militar Ambiental é responsável por fiscalizar e combater crimes
      ambientais em cada estado do Brasil. É possível encontrar o telefone de contato
      da Polícia Ambiental do seu estado no site da Polícia Militar.
    </li>
    <li>
      <strong>Ministério Público:</strong>
      o Ministério Público é responsável pela defesa da ordem jurídica, dos direitos
      sociais e dos interesses coletivos e difusos. É possível denunciar crimes ambientais
      diretamente ao Ministério Público do seu estado.
    </li>
    <li>
      <strong>ONGs de proteção animal:</strong>
      diversas organizações não governamentais atuam na proteção da fauna brasileira
      e podem receber denúncias de maus-tratos e tráfico de animais. Alguns exemplos
      são a <strong>Sociedade Protetora dos Animais (SPA)</strong>, a
      <strong>Sociedade Brasileira de Proteção Animal (SBPA)</strong>
      e a <strong>Sociedade Mundial de Proteção Animal (WSPA)</strong>.
    </li>
  </ul>
</div>

