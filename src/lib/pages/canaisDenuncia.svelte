  <h1>CANAIS DE DENÚNCIA EM SANTA CATARINA</h1>

  <p>
    Em Santa Catarina, as denúncias de maus tratos e animais em cativeiro podem
    ser feitas através do Centro Integrado de Operações de Segurança Pública
    (CIOSP), pelo número <strong>190</strong>, através da Polícia Militar
    Ambiental, pelo número
    <strong>(48) 3665-4800</strong>, ou em contato com a Polícia Federal, através
    do telefone (48) 3281-6500, que também atua no combate ao tráfico de animais
    silvestres.
  </p>
  <p>
    A Secretaria do Meio Ambiente do estado de Santa Catarina (SMA) também possui
    canais de denúncia, que podem ser acessados através do telefone (48) 3665-4175
    ou pelo e-mail denuncia.sema@sema.sc.gov.br. É importante lembrar que as
    denúncias podem ser feitas de forma anônima e que a proteção dos animais é um
    dever de todos.
  </p>
